// Generates media/vditor-icons-codicon.js — a runtime override that restyles the
// Vditor toolbar to codicons. It loads AFTER vditor's icon script (ant.js), which
// synchronously injects all <symbol id="vditor-icon-…"> defs; this script then
// mutates the symbols we want (codicon paths + 6 custom glyphs drawn in codicon
// style) in place by id. Untouched symbols keep their ant glyph. See task 44.
//
// Source of truth: media-src/icons/<name>.svg, where <name> maps to the Vditor
// symbol id `vditor-icon-<name>`. Each is a 16×16 codicon-style glyph.
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const here = path.dirname(fileURLToPath(import.meta.url))
const ICONS = path.join(here, 'icons')
const OUT = path.resolve(here, '..', 'media', 'vditor-icons-codicon.js')

function innerOf(svg) {
  // strip the outer <svg …> … </svg> wrapper, keep the inner markup (path/g)
  return svg
    .replace(/^[\s\S]*?<svg[^>]*>/i, '')
    .replace(/<\/svg>\s*$/i, '')
    .replace(/\s+/g, ' ')
    .trim()
}

const overrides = {}
for (const file of fs
  .readdirSync(ICONS)
  .filter((f) => f.endsWith('.svg'))
  .sort()) {
  const id = `vditor-icon-${file.replace(/\.svg$/, '')}`
  overrides[id] = innerOf(fs.readFileSync(path.join(ICONS, file), 'utf8'))
}

const banner =
  '// AUTO-GENERATED by media-src/build-icon-sprite.mjs — do not edit by hand.\n'
const body =
  '(function(){var O=' +
  JSON.stringify(overrides) +
  ';function apply(){for(var id in O){var s=document.getElementById(id);' +
  "if(s){s.setAttribute('viewBox','0 0 16 16');s.innerHTML=O[id];}}}" +
  "if(document.getElementById('vditor-icon-bold')){apply();}" +
  "else{document.addEventListener('DOMContentLoaded',apply);}})();\n"

fs.writeFileSync(OUT, banner + body)
console.log(
  `icon sprite: ${Object.keys(overrides).length} codicon overrides → ${path.relative(path.resolve(here, '..'), OUT)}`,
)
