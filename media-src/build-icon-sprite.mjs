// Generates media/vditor-icons.js — the SINGLE icon sprite the webview loads
// (as id="vditorIconScript", which makes Vditor skip loading its own ant.js).
//
// It is Vditor's `ant` symbol set with our 30 toolbar glyphs swapped for codicons
// (24 official codicons + 6 codicon-style customs), baked at build time. Every
// other Vditor symbol (table-panel align/insert/delete, resize, trashcan, …) is
// kept verbatim from ant so nothing else loses its icon. This replaces the old
// load-ant-then-mutate-at-runtime approach: one injected file, zero runtime DOM
// mutation. See task 44.
//
// Sources: media-src/node_modules/vditor/dist/js/icons/ant.js (full symbol set)
// + media-src/icons/<name>.svg (our codicon overrides for vditor-icon-<name>).
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const here = path.dirname(fileURLToPath(import.meta.url))
const ICONS = path.join(here, 'icons')
const ANT = path.join(
  here,
  'node_modules',
  'vditor',
  'dist',
  'js',
  'icons',
  'ant.js',
)
const OUT = path.resolve(here, '..', 'media', 'vditor-icons.js')

// Strip the outer <svg …> … </svg> wrapper, keep the inner markup (path/g).
function innerOf(svg) {
  return svg
    .replace(/^[\s\S]*?<svg[^>]*>/i, '')
    .replace(/<\/svg>\s*$/i, '')
    .replace(/\s+/g, ' ')
    .trim()
}

// Our codicon overrides, keyed by symbol id (vditor-icon-<file>).
const overrides = {}
for (const file of fs.readdirSync(ICONS).filter((f) => f.endsWith('.svg'))) {
  const id = `vditor-icon-${file.replace(/\.svg$/, '')}`
  overrides[id] = innerOf(fs.readFileSync(path.join(ICONS, file), 'utf8'))
}

// Every symbol Vditor's ant set defines, in source order; ours swapped in.
const ant = fs.readFileSync(ANT, 'utf8')
const symbolRe =
  /<symbol id="(vditor-icon-[a-z0-9-]+)"([^>]*)>([\s\S]*?)<\/symbol>/g
const symbols = []
let overridden = 0
let m = symbolRe.exec(ant)
while (m !== null) {
  const [, id, attrs, inner] = m
  if (overrides[id] !== undefined) {
    symbols.push(
      `<symbol id="${id}" viewBox="0 0 16 16">${overrides[id]}</symbol>`,
    )
    overridden++
  } else {
    symbols.push(
      `<symbol id="${id}"${attrs}>${inner.replace(/\s+/g, ' ').trim()}</symbol>`,
    )
  }
  m = symbolRe.exec(ant)
}
if (!symbols.length) throw new Error('no vditor-icon symbols found in ant.js')

const banner =
  '// AUTO-GENERATED by media-src/build-icon-sprite.mjs — do not edit by hand.\n'
const svg =
  '<svg style="position:absolute;width:0;height:0;overflow:hidden" version="1.1" xmlns="http://www.w3.org/2000/svg"><defs>' +
  symbols.join('') +
  '</defs></svg>'
const body = `document.body.insertAdjacentHTML("afterbegin", ${JSON.stringify(svg)});\n`

fs.writeFileSync(OUT, banner + body)
console.log(
  `icon sprite: ${symbols.length} symbols (${overridden} codicon, ${symbols.length - overridden} ant) → ${path.relative(path.resolve(here, '..'), OUT)}`,
)
